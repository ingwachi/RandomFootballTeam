package sample;

public class MidFielder {
    private String name;
    private String numShirt;

    public MidFielder(String name, String numShirt) {
        this.name = name;
        this.numShirt = numShirt;
    }
    public String getName() {
        return name;
    }

    public String getNumShirt() {
        return numShirt;
    }
}
