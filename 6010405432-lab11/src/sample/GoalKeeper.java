package sample;

public class GoalKeeper {
    private String name;
    private String numShirt;

    public GoalKeeper(String name, String numShirt) {
        this.name = name;
        this.numShirt = numShirt;
    }

    public String getName() {
        return name;
    }

    public String getNumShirt() {
        return numShirt;
    }
}
